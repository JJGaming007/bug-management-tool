const navItems = [
  { label: 'Dashboard', href: '/', icon: DashboardIcon },
  { label: 'Bugs', href: '/bugs', icon: BugIcon },
  { label: 'Board', href: '/board', icon: BoardIcon },
  { label: 'Backlog', href: '/backlog', icon: BacklogIcon },
  { label: 'Sprints', href: '/sprints', icon: SprintIcon },
]

const manageItems = [
  { label: 'Projects', href: '/projects', icon: ProjectIcon },
  { label: 'Organizations', href: '/organizations', icon: OrganizationIcon },
]

const settingsItems = [
  { label: 'Settings', href: '/account', icon: SettingsIcon },
]

export default function Sidebar() {
  const pathname = usePathname() ?? '/'
  const { isOpen, close } = useSidebar()

  const isActive = (href: string) => {
    if (href === '/') {
      return pathname === '/' || pathname === '/dashboard'
    }
    return pathname === href || pathname?.startsWith(href + '/')
  }

  return (
    <>
      <div className={`sidebar ${isOpen ? 'open' : ''}`}>
        <div className="sidebar-header">
        <div className="sidebar-brand">
          <div className="sidebar-logo">BT</div>
          <span className="sidebar-title">BugTracker</span>
        </div>
      </div>

      <nav className="sidebar-nav">
        <div className="nav-section">
          <div className="nav-section-title">Menu</div>
          {navItems.map((item) => {
            const Icon = item.icon
            const active = isActive(item.href)
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`nav-item ${active ? 'active' : ''}`}
                onClick={close}
              >
                <Icon />
                <span>{item.label}</span>
              </Link>
            )
          })}
        </div>

        <div className="nav-section">
          <div className="nav-section-title">Manage</div>
          {manageItems.map((item) => {
            const Icon = item.icon
            const active = isActive(item.href)
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`nav-item ${active ? 'active' : ''}`}
                onClick={close}
              >
                <Icon />
                <span>{item.label}</span>
              </Link>
            )
          })}
        </div>

        <div className="nav-section">
          <div className="nav-section-title">Account</div>
          {settingsItems.map((item) => {
            const Icon = item.icon
            const active = isActive(item.href)
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`nav-item ${active ? 'active' : ''}`}
                onClick={close}
              >
                <Icon />
                <span>{item.label}</span>
              </Link>
            )
          })}
        </div>
      </nav>

        <div className="sidebar-footer">
          <div style={{ fontSize: '12px', color: 'var(--text-muted)' }}>
            BugTracker Pro v1.0
          </div>
        </div>
      </div>
      {isOpen && <div className="sidebar-overlay" onClick={close} />}
    </>
  )
}
